public class TestaWhile {
    public static void main(String[] args) {
        int contadorPC = 0;
        while (contadorPC <= 10){ //O WHILE ou ENQUANTO. Indica que até que a condição desejada seja alcanda, haverá um loop do que estiver na sua condição
            System.out.println(contadorPC);
            //contadorPC = contadorPC + 1; Ambos os metodos podem ser usados e retornarão o mesmo resultado
            //contadorPC += 1;
            contadorPC ++;
        }
    }
}
